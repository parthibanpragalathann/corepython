'''

Assignment Operators are used to assigning value to a variable

'''
# Assignment Operators

"""
=   Assignment
+=	Addition
-=	Subtraction
*=	Multiplication
/=	Division
%=	Modulus
**=	Exponentiation
//=	Floor division
"""
ass_val=110
print("Assignment : ass_val=110 : ", ass_val)
ass_val += 10
print("Assignment Addition : ass_val += 10 : ", ass_val)
ass_val -= 100
print("Assignment Subtraction : ass_val -= 100 : ", ass_val)
ass_val *= 10
print("Assignment Multiplication : ass_val *= 10 : ", ass_val)
ass_val /= 10
print("Assignment Division : ass_val /= 10 : ", ass_val)
ass_val %= 2
print("Assignment Modulus : ass_val %= 2 : ", ass_val)
ass_val = 10
ass_val **= 5
print("Assignment Exponentiation : ass_val **= 5 : ", ass_val)
ass_val //= 2
print("Assignment Floor division : ass_val //= 2 : ", ass_val)