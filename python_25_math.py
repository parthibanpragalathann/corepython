'''
MATH FUNCTION IN PYTHON

The math module is used to access mathematical functions in the Python.
All methods of this functions are used for integer or real type objects, not for complex numbers.
'''


#Source Code
import math
print("square root of ", math.sqrt(8))
print("power of ", math.pow(6, 2))
# 5! => 1*2*3*4*5
print("factorial  is  ", math.factorial(5))
print("greatest whole number of given number ", math.ceil(1.55555))
print("eliminate the digit number ", math.floor(1.55555))
print("absolute numbers only return positive number", math.fabs(-5))
print("log2 of 2 is ", math.log2(2))
print("log10 of 2 is ", math.log10(2))
print("constant pi value ", math.pi)
print("constant e value ", math.e)

