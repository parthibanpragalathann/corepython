'''
MEMBERSHIP Operator in python
object present or not in the sequence of variable

'''

a=[1, 11, 111, 1111]
print("a sequence values ", a)
print("object 11 in sequence variable a ", 11 in a)
print("object 10 in sequence variable a ", 10 in a)
print("object 11 not in sequence variable a ", 11 in a)
print("object 10 not in sequence variable a ", 10 in a)

'''
OUTPUT
a sequence values  [1, 11, 111, 1111]
object 11 in sequence variable a  True
object 10 in sequence variable a  False
object 11 not in sequence variable a  True
object 10 not in sequence variable a  False

'''